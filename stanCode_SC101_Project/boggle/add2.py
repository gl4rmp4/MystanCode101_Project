import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    cur1 = l1
    cur2 = l2
    l1_str = ''
    l2_str = ''
    while cur1 is not None:
        if l1_str == '':
            l1_str += str(cur1.val)
        else:
            l1_str = str(cur1.val) + l1_str
        cur1 = cur1.next
    while cur2 is not None:
        if l2_str == '':
            l2_str += str(cur2.val)
        else:
            l2_str = str(cur2.val) + l2_str
        cur2 = cur2.next
    l3_int = int(l1_str) + int(l2_str)
    l3_str = ''
    for number in str(l3_int):
        if l3_str =='':
            l3_str += number
        else:
            l3_str = number + l3_str
    l3 = None
    for data in l3_str:
        new_node = ListNode(int(data), None)
        if l3 is None:
            # first data
            l3 = new_node
            cur3 = l3
        else:
            cur3.next = new_node
            cur3 =cur3.next
    #######################
    return l3


